class Parrot:

    # class attribute
    name = ""
    age =0

#create parrot1 object
parrot1 = Parrot()
parrot1.name = "Blu"
parrot1.age =10

#create parrot2 object
parrot2 = Parrot()
parrot2.name = "Woo"
parrot2.age = 103

# access attributes
print(f"{parrot1.name} is {parrot1.age} years old" )
print(f"{parrot2.name} is {parrot2.age} years old" )








































































































































































































